<template>
  <div class="com-container">
    <div class="com-chart" ref="stock_ref"></div>
  </div>
</template>

<script>
// import { mapState } from "vuex";
import * as echarts from 'echarts'
import {markRaw} from 'vue'
import {reqgetVolunteerVolumn} from '../../../api'
export default {
  name: "Stock",
  data() {
    return {
      chartInstance: null,
      allData: null,
      timeId: null,
      currentIndex: 0,
    };
  },
  computed: {
    // ...mapState(["theme"]),
  },
  created() {
    // 当浏览器获取到服务器发送过来的数据时就会调用getData方法
    // this.$socket.registerCallBack("stockData", this.getData);
  },
  mounted() {
    this.initChart();
    this.getData();
    // this.$socket.send({
    //   action: "getData",
    //   socketType: "stockData",
    //   chartName: "stock",
    //   value: "",
    // });
    window.addEventListener("resize", this.screenAdapter);
    this.screenAdapter();
  },
  beforeDestroy() {
    window.removeEventListener("resize", this.screenAdapter);
    clearInterval(this.timeId);
    // this.$socket.unRegisterCallBack("stockData");
  },
  methods: {
    initChart() {
      this.chartInstance = markRaw(echarts.init(this.$refs.stock_ref));
      const initOption = {
        title: {
          text: "▎志愿相关统计量",
          left: 20,
          top: 20,
        },
      };
      this.chartInstance.setOption(initOption);
      this.chartInstance.on("mouseover", () => {
        clearInterval(this.timeId);
      });
      this.chartInstance.on("mouseout", () => {
        this.startInterval();
      });
    },
    // data：服务器发送过来的图表的数据
    async getData() {
      let res =await reqgetVolunteerVolumn()
      // this.$http.get("https://mock.apipark.cn/m1/4435779-0-default/ttt").then((response) => {
      this.allData = res
      // console.log(response.data.data)
      this.updateChart();
      this.startInterval();
      // }).catch((error) => {
      //   console.error("请求后端接口出错：", error);
      // });
    },
    updateChart() {
      const centerArr = [
        ["18%", "40%"],
        ["50%", "40%"],
        ["82%", "40%"],
        ["34%", "75%"],
        ["66%", "75%"],
      ];
      const colorArr = [
        ["#4FF778", "#0BA82C"],
        ["#E5DD45", "#E8811C"],
        ["#E8821C", "#E55445"],
        ["#5052EE", "#AB6EE5"],
        ["#23E5E5", "#2E72BF"],
      ];
      // 数据处理
      const start = this.currentIndex * 5;
      const end = (this.currentIndex + 1) * 5;
      const showData = this.allData.slice(start, end);
      const seriesArr = showData.map((item, index) => {
        return {
          type: "pie",
          // radius: [110, 100],
          center: centerArr[index],
          data: [
            {
              value: item.sales,
              name: item.name + "\n\n" + item.sales,
              itemStyle: {
                color: new echarts.graphic.LinearGradient(0, 1, 0, 0, [
                  {
                    offset: 0,
                    color: colorArr[index][0],
                  },
                  {
                    offset: 1,
                    color: colorArr[index][1],
                  },
                ]),
              },
            },
            {
              value: item.stock,
              itemStyle: {
                color: "#333843",
              },
            },
          ],
          // 关闭鼠标移入饼图时的动画效果
          hoverAnimation: false,
          label: {
            position: "center",
            color: colorArr[index][0],
          },
          labelLine: {
            show: false,
          },
        };
      });
      const dataOption = {
        series: seriesArr,
      };
      this.chartInstance.setOption(dataOption);
    },
    screenAdapter() {
      const titleFontSize = (this.$refs.stock_ref.offsetWidth / 100) * 3.6;
      const innerRadius = titleFontSize * 2.8;
      const outterRadius = innerRadius * 1.125;
      const adapterOption = {
        title: {
          textStyle: {
            fontSize: titleFontSize,
          },
        },
        series: [
          {
            type: "pie",
            radius: [outterRadius, innerRadius],
            label: {
              fontSize: titleFontSize / 2,
            },
          },
          {
            type: "pie",
            radius: [outterRadius, innerRadius],
            label: {
              fontSize: titleFontSize / 2,
            },
          },
          {
            type: "pie",
            radius: [outterRadius, innerRadius],
            label: {
              fontSize: titleFontSize / 2,
            },
          },
          {
            type: "pie",
            radius: [outterRadius, innerRadius],
            label: {
              fontSize: titleFontSize / 2,
            },
          },
          {
            type: "pie",
            radius: [outterRadius, innerRadius],
            label: {
              fontSize: titleFontSize / 2,
            },
          },
        ],
      };
      this.chartInstance.setOption(adapterOption);
      this.chartInstance.resize();
    },
    startInterval() {
      if (this.timeId) clearInterval(this.timeId);
      this.timeId = setInterval(() => {
        this.currentIndex++;
        if (this.currentIndex > 1) this.currentIndex = 0;
        this.updateChart();
      }, 5000);
    },
  },
  watch: {
    // theme() {
    //   // console.log("Stock 主题切换了！");
    //   this.chartInstance.dispose(); // 销毁当前的图表
    //   this.initChart(); // 重新以最新的主题名称初始化图表对象
    //   this.screenAdapter(); // 完成屏幕的适配
    //   this.updateChart(); // 更新图表的展示
    // },
  },
};
</script>
